#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char *ts;

	ts = (char*)s;
	while (n > 0)
	{
		if (c != *ts)
		{
			ts++;
			n--;
		}
		else
			return (ts);
	}
	return (0);
}
